package com.projects.campout.J.model.api.entity;

public class Provider {

  private String name;

  private String attributionText;

  private String attributionLink;

  private String attributionImage;


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getAttributionText() {
    return attributionText;
  }

  public void setAttributionText(String attributionText) {
    this.attributionText = attributionText;
  }

  public String getAttributionLink() {
    return attributionLink;
  }

  public void setAttributionLink(String attributionLink) {
    this.attributionLink = attributionLink;
  }

  public String getAttributionImage() {
    return attributionImage;
  }

  public void setAttributionImage(String attributionImage) {
    this.attributionImage = attributionImage;
  }
}
